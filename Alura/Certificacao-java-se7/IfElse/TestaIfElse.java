package IfElse;

public class TestaIfElse {
  public static void main(String[] args) {
    boolean autentico = true;

    if (autentico) {
      System.out.println("OK");
    } else {
      System.out.println("Não autorizado.");
    }

    boolean valor = true;

    if (valor == false) {
      System.out.println("é false");
    }
  }
}
