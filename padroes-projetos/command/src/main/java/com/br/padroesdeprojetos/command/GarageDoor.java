package com.br.padroesdeprojetos.command;

public class GarageDoor {

    public GarageDoor() {}

    public void up() {
        System.out.println("garage door up!");
    }

    public void down() {
        System.out.println("garage door down!");
    }

    public void stop() {
        System.out.println("garage door stopping!");
    }

    public void lightOn(){
        System.out.println("garage light on!");
    }

    public void lightOff() {
        System.out.println("garage light off!");
    }
}
